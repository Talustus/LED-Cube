#include <stdint.h>
#include <avr/io.h>
#include <avr/pgmspace.h>

uint8_t font[95][8];
uint8_t fontBuff[8];

uint8_t *getFont(char c) {
	uint8_t i;
	if (!((c >= 0x20) && (c <= 0x7E))) {
		c = '?';
	}
	for (i = 0; i < 8; i++) {
		fontBuff[i] = pgm_read_byte(&(font[(uint8_t)(c - 0x20)][i]));
	}
	return fontBuff;
}


